namespace YoYo.Model
{
    public class TestAthleteParam
    {
        /// <summary>
        /// Gets or sets athlete id.
        /// </summary>
        public int AthleteId { get; set; }

        /// <summary>
        /// Gets or sets current shuttle speed level.
        /// </summary>
        public int SpeedLevel { get; set; }

        /// <summary>
        /// Gets or sets the current shuttle level.
        /// </summary>
        public int ShuttleLevel { get; set; }

        /// <summary>
        /// Gets or sets current shuttle number.
        /// </summary>
        public int ShuttleNumber { get; set; }

        /// <summary>
        /// Gets or sets selected test score.
        /// </summary>
        public string TestScore { get; set; }
    }
}
